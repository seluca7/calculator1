public class Calculator {
    public int addition(int first, int second){
        return first+second;
    }

    public float addition(float first, float second){
        return first+second;
    }

    public int addition(int first, int second, int third){
        return first + second + third;
    }
    public int subtraction(int first, int second){
        return first-second;
    }

    public float subtraction(float first, float second){
        return first - second;
    }

    public int subtraction(int first, int second, int third){
        return first-second-third;
    }

    public int multiplication(int first, int second){
        return  first*second;
    }
    public float multiplication(float first, float second){
        return first*second;
    }
    public double multiplication(double first, double second){
        return first*second;
    }
    public int multiplication(int first, int second, int third){
        return first*second*third;
    }
    public double division(double first, double second){
        return first/second;
    }
    public int division(int first, int second){
        return first/second;
    }
    public float division(float first, float second){
        return first/second;
    }
}
